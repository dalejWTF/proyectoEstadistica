    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:

        double array[]= new double[cbVariables.getSelectedIndex()+1];
        if(array.length==1){
            array[0]=Double.parseDouble(txtPA.getText());
        }else if(array.length==2){
            array[0]=Double.parseDouble(txtPA.getText());
            array[1]=Double.parseDouble(txtPB.getText());
        }else if(array.length==3){
            array[0]=Double.parseDouble(txtPA.getText());
            array[1]=Double.parseDouble(txtPB.getText());
            array[2]=Double.parseDouble(txtPC.getText());
        }else if(array.length==4){
            array[0]=Double.parseDouble(txtPA.getText());
            array[1]=Double.parseDouble(txtPB.getText());
            array[2]=Double.parseDouble(txtPC.getText());
            array[3]=Double.parseDouble(txtPD.getText());
        }else if(array.length==5){
            array[0]=Double.parseDouble(txtPA.getText());
            array[1]=Double.parseDouble(txtPB.getText());
            array[2]=Double.parseDouble(txtPC.getText());
            array[3]=Double.parseDouble(txtPD.getText());
            array[4]=Double.parseDouble(txtPE.getText());
        }
        ProbSubEMUL obj= new ProbSubEMUL(array);
        obj.Calcular();
        lblResultado.setText(obj.toString());
    }//GEN-LAST:event_btnCalcularActionPerformed

